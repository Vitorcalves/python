print(nome_curso.strip())
# remove os espacos em branco
print(nome_curso.lstrip())
# remove os espacos a esquerda
print(nome_curso.rstrip())
# remove os espacos a direita
print(nome_curso.find('cao'))
# mostra a posicao da palavra
print(nome_curso.replace('Video', 'Audio'))
